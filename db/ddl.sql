CREATE TABLE LAW_MASTER (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    LAW_ID VARCHAR2(50) UNIQUE,
    LAW_NAME VARCHAR2(200),
    LAW_TYPE VARCHAR2(20)
);

CREATE TABLE LAW_VERSION (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    LAW_MASTER_ID NUMBER,
    VERSION_NO NUMBER,
    CONTENT CLOB,
    CONTENT_HASH VARCHAR2(64),
    CREATED_AT DATE DEFAULT SYSDATE
);